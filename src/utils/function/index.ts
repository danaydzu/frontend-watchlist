import {ICheckData} from "../../common/types/auth";
import {AppErrors} from "../../common/errors";

export const checkErrorsData = ({firstName, username, password, repeatPassword, email}: ICheckData): void => {


    if (!(/(?:[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*|"(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21\x23-\x5b\x5d-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])*")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\[(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?|[a-z0-9-]*[a-z0-9]:(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21-\x5a\x53-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])+)\])/
        .test(email))) throw new Error(AppErrors.IncorrectEmailAddress)

    if (!(password === repeatPassword)) throw new Error(AppErrors.PasswordDoNotMatch)

    if (!(username.length > 1)) throw new Error(AppErrors.UsernameLessOneCharacter)

    if (!(firstName.length > 1)) throw new Error(AppErrors.FirstNameLessOneCharacter)


}